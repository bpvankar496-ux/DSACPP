// ## 1. Time Complexity shu che?
// Code run karva ma **ketlo time** lage — e input size sathe **ketle rate e vadhse** teni measure.
// > Clock time nai, **steps/operations** count kariye.

// ## 2. Constant Time — O(1)
// Input size **game teto hoy**, operations **same** rehse.
// > Array ma pehlo element access karo — `arr[0]` — 1 j step, n=10 hoy ke n=1000.

// ## 3. Big O Notation
// **Worst case** ma algorithm **ketla operations** karse teni upper limit.
// > Tame guarantee karo ke "aa thi vadhu nai lagse."

// ## 4. Big O from Functions
// Code joine directly Big O kadhvo — loops, nested loops joiye.
// > Single loop → O(n), nested loop → O(n²)

// ## 5. Other Notations
// - **Big O** → Worst case (most used)
// - **Omega Ω** → Best case
// - **Theta Θ** → Average case

// ## 6. Space Complexity
// Code run karva ma **ketli memory** vaprase.
// > Extra array banavo to O(n) space, sirf variables hoy to O(1) space.

// ## 7. Common Time Complexities (best to worst)
// | Complexity | Name |
// |------------|------|
// | O(1) | Constant |
// | O(log n) | Logarithmic |
// | O(n) | Linear |
// | O(n log n) | Linearithmic |
// | O(n²) | Quadratic |
// | O(2ⁿ) | Exponential |

// ## 8. Constant & Linear TC
// - **O(1)** → koi loop nai, direct access
// - **O(n)** → ek loop, n vaar chaale

// ## 9. Quadratic TC — O(n²)
// **Nested loops** — ek loop andar bijo loop, banne n vaar chaale.

// ## 10. Logarithmic TC — O(log n)
// Har step ma **problem half** thay jase.
// > Binary search — 1000 elements ma sirf ~10 steps!



